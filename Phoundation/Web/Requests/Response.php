    /**
     * Load the specified JavaScript file(s) by adding them to the "headers" or "footers" list
     *
     * @param string|array $urls
     * @param bool|null    $header
     * @param bool         $prefix If true, the scripts are added at the beginning of the "headers" or "footers" list
     *
     * @return void
     */
    public static function loadJavaScript(string|array $urls, ?bool $header = null, bool $prefix = false): void
    {
        if ($header === null) {
            $header = !config()->getBoolean('web.javascript.delay', true);
        }

        if ($header and static::$html_headers_sent) {
            Log::warning(ts('Not adding files ":files" to HTML headers as the HTML headers have already been generated', [
                ':files' => $urls,
            ]));
        }

        $scripts = [];

        // Convert the given URL (parts) to real URLs and add it to the "scripts" list
        foreach (Arrays::force($urls, ',') as $url) {
            if (!Url::isValidUrl($url)) {
                // Pre-process local URL's
                $url = Strings::ensureEndsNotWith($url, '.js');
                $url = Strings::ensureEndsNotWith($url, '.min');
                $url = Response::versionFile($url, 'js');
            }

            $scripts[$url] = [
                'type' => 'text/javascript',
                'src'  => Url::new($url)->makeJs(),
            ];
        }

        // Add scripts to header or footer
        if ($header) {
            Response::addPageHeaders(EnumHeaderFooterType::javascript, $scripts, $prefix);

        } else {
            Response::addPageFooters(EnumHeaderFooterType::javascript, $scripts, $prefix);
        }
    }


