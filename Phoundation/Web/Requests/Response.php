    /**
     * Set the favicon for this page
     *
     * @param string|null $url
     *
     * @return void
     */
    public static function setFavIcon(?string $url = null): void
    {
        try {
            if (empty($url)) {
                $_file = PhoPath::new('img/favicons/project.png', PhoRestrictions::newReadonlyObject(DIRECTORY_PROJECT_CDN), DIRECTORY_PROJECT_CDN);
                $_url  = Url::newFromPath($_file)->makeImg();
                $url    = $_url->getSource();

                Response::addLinkToPageHeaders([
                    'rel'  => 'icon',
                    'href' => $url,
                    'type' => $_file->getMimetype(),
                ], $url);

            } else {
                $_url = Url::new($url)->makeImg();
                $_url = $_url->setSource(Response::versionFile($url, 'img'));
                $url   = $_url->getSource();

                // Unknown (likely remote?) link
                Response::addLinkToPageHeaders([
                    'rel'  => 'icon',
                    'href' => $url,
                    'type' => 'image/' . Strings::fromReverse($url, '.'),
                ], $url);
            }

            Response::setAppleIcons($url);

        } catch (FilesystemException $e) {
            Log::warning(ts('Failed to find favicon, see next message for more information'), 3);
            Log::warning($e->makeWarning(), 3);
        }
    }


    /**
     * Adds apple touch icons to the site with correct locations
     *
     * @param string $url
     *
     * @return void
     */
    protected static function setAppleIcons(string $url): void
    {
        // Do we even want to add these apple touch icons?
        if (!config()->getBoolean('web.icons.apple.enable', false)) {
            return;
        }

        // The sizes we need to support, jeeeezz...
        $sizes = [
            '57x57',
            '60x60',
            '72x72',
            '76x76',
            '114x114',
            '120x120',
            '144x144',
            '152x152',
            '180x180',
        ];

        // apple icons should be in the same path as the favicons
        $url = Strings::untilReverse($url, '/') . '/';

        foreach ($sizes as $size) {
            Response::addLinkToPageHeaders([
                'rel'   => 'apple-touch-icon',
                'sizes' => $size,
                'href'  => Url::new($url . 'apple-touch-icon-' . $size . '.png')->makeImg(),
                'type'  => 'image/png', // Require these files to always be PNG as we do not want to do a mimetype lookup because apple likes being a ...
            ], 'apple-touch-icon-' . $size . '.png');

            if (config()->getBoolean('web.icons.apple.precomposed', true)) {
                // Because of course, lets add an entire other set too just for apple, why not?
                Response::addLinkToPageHeaders([
                    'rel'   => 'apple-touch-icon-precomposed',
                    'sizes' => $size,
                    'href'  => Url::new($url . 'apple-touch-icon-' . $size . '-precomposed.png')->makeImg(),
                    'type'  => 'image/png', // Require these files to always be PNG as we do not want to do a mimetype lookup because apple likes being a ...
                ], 'apple-touch-icon-' . $size . '-precomposed.png');
            }
        }
    }


    /**
     * Will automatically add the timestamp of the specified file as a versioning string
     *
     * This is done for efficient caching where you can pretty much set cache to 10 years as changes are picked up by
     * updated versions of the files
     *
     * @see http://particletree.com/notebook/automatically-version-your-css-and-javascript-files/
     *
     * @todo Move this to the URL class, and be probably used somewhere in the ->makeCdn() call?
     * @param string $url
     * @param string $type
     *
     * @return string
     */
    public static function versionFile(string $url, string $type): string
    {
        static $minified = null;

        // Ensure the extension is stripped.
        // Ensure anything before templates/ is stripped.
        $url = Strings::untilReverse($url, '.' . $type);
        $url = Strings::until($url, '.min');
        $url = Strings::from($url, 'templates/');

        if ($minified === null) {
            // All files are minified or none are
            $minified = (config()->get('web.cdn.resources.minified', true) ? '.min' : '');
        }

        // Return URL with timestamp injected into the given file
        if (config()->getBoolean('web.cdn.resources.versioning', true)) {
            // Determine the absolute file path
            return $url . '-v' . filectime(DIRECTORY_DATA . 'content/cdn/' . LANGUAGE . '/templates/' . $url . $minified . '.' . $type) . $minified . '.' . $type;
        }

        // Return URL without an injected timestamp
        return $url . $minified . '.' . $type;
    }


