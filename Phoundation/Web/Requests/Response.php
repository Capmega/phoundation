    /**
     * Check if this user should be forcibly being redirected to a different page
     *
     * @return void
     */
    public static function checkForceRedirect(): void
    {
        // Does this user have a forced redirect?
        if (!Session::getUserObject()->isGuest()) {
            $redirect = Session::getUserObject()->getRedirect();

            if ($redirect) {
                // Are we at the forced redirect page? If so, we can stay
                $current = (string) Url::newCurrent();

                if (Strings::until($redirect, '?') !== Strings::until($current, '?')) {
                    // We are at a different page. Should we redirect to the specified page?
                    if (!Response::skipRedirect()) {
                        // No, it is not, redirect!
                        Log::action(ts('User ":user" has a redirect to ":url", redirecting there instead', [
                            ':user' => Session::getUserObject()->getLogId(),
                            ':url'  => $redirect,
                        ]));

                        // Get URL builder object, ensure that sign-in page gets a redirect=$current_url
                        $redirect = Url::new($redirect)->makeWww();

                        if ((string) $redirect === (string) Url::new('sign-in')->makeWww()) {
                            $redirect->addRedirect($current);
                        }

                        Response::redirect($redirect);
                    }

                    Log::warning(ts('User ":user" has a redirect to ":url" which MAY NOT redirected to, ignoring redirect', [
                        ':user' => Session::getUserObject()->getLogId(),
                        ':url'  => $redirect,
                    ]));
                }
            }
        }
    }


