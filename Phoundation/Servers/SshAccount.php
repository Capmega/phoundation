<?php

/**
 * SshAccount class
 *
 * This DataEntry type class represents a single entry in the ssh_accounts table.
 *
 * This DataEntry class supports configuration loading from the path "ssh.accounts"
 *
 * Upon loading a new entry, if the column "file" is specified, but the "ssh_key" column is empty, the class will
 * automatically inject the contents of said file into the column "ssh_key"
 *
