    /**
     * Undelete the specified entries
     *
     * @note This will set the status "NULL" to the entries in this datalist, NOT the original value of their status!
     *
     * @param string|null $comments
     * @param bool        $auto_save
     *
     * @return static
     */
    public function undelete(?string $comments = null, bool $auto_save = true): static
    {
        return $this->setStatus(null, $comments, $auto_save);
    }


    /**
     * Set the specified status for the specified entries
     *
     * @param string|null $status
     * @param string|null $comments
     * @param bool        $auto_save
     *
     * @return static
     */
    public function setStatus(?string $status, ?string $comments = null, bool $auto_save = true): static
    {
        foreach ($this->source as $entry) {
            $entry->setStatus($status, $comments, $auto_save);
        }

        return $this->setModifiedCount(count($this->source));
    }


    /**
     * Erase (as in SQL DELETE) the specified entries from the database, also erasing their meta data
     *
     * @return static
     */
    public function erase(): static
    {
        $ids  = [];
        $meta = [];

        // Delete the meta data entries
        foreach ($this->source as $id => $entry) {
            $ids[] = $id;

            if (is_array($entry)) {
                $meta[] = $entry['meta_id'];

            } elseif ($entry instanceof DataEntryInterface) {
                $meta[] = $entry->getMetaId();
            }
        }

        if ($ids) {
            // Delete the meta data
            Meta::eraseEntries($meta);

            // Delete the entries themselves
            sql($this->getConnectorObject())->setDebug($this->debug)
                                            ->erase(static::getTable(), ['id' => $ids]);
        }

        return $this;
    }


    /**
     * Deletes ALL entries in this table!
     *
     * This method reads in $size entries at the time, erases them, and continues onto the next until all are erased
     *
     * @param int $size
     *
     * @return void
     */
    public static function truncate(int $size = 10): void
    {
        Log::action(tr('Truncating table ":table"', [':table' => static::getTable()]), echo_newline: false);

        do {
            $iterator = static::new()->setQuery('SELECT * FROM `' . static::getTable() . '` LIMIT ' . $size);
            $iterator->erase();
            Log::dot(1);

        } while ($iterator->getCount());

        Log::success(tr('Done!'));

        Incident::new()
                ->setType('sysop')
                ->setTitle(ts('Mobile Billing Reset'))
                ->setBody(ts('User ":user" performed a truncate on table ":table"', [
                    ':user'  => Session::getUserObject()->getDisplayName(),
                    ':table' => static::getTable()
                ]))
                ->setNotifyRoles('developer')
                ->save();
    }


