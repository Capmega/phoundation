    /**
     * Returns what SQL columns will be used in loading data
     *
     * @return string
     */
    public function getSqlSelectColumns(): string
    {
        if ($this->columns) {
            $return = [];

            // Add SQL SELECT for each specified column
            foreach ($this->columns as $column) {
                $return[] = SqlQueries::ensureQuotes(static::getTable()) . '.' . SqlQueries::ensureQuotes($column);
            }

            return implode(', ', $return);
        }

        // Load all columns
        return SqlQueries::ensureQuotes(static::getTableIdColumn()) . ' AS `unique_identifier`, ' . SqlQueries::ensureQuotes(static::getTable()) . '.* ';
    }


    /**
     * Returns all data for this data entry at once with values being the specified column
     *
     * @note This method filters out all keys defined in static::getProtectedKeys() to ensure that keys like "password"
     *       will not become available outside this object
     *
     * @see EntryCore::getSource()
     * @param string $column
     *
     * @return array
     */
    public function getSourceColumn(string $column): array
    {
        $source = $this->getSource();

        foreach ($source as &$value) {
            if (is_array($value)) {
                $value = $value[$column];

            } elseif ($value instanceof EntryInterface) {
                $value = $value->get($column);

            } else {
                throw new OutOfBoundsException(tr('Cannot return specified column ":column", the Iterator source contains invalid value ":value"', [
                    ':column' => $column,
                    ':value'  => $value
                ]));
            }
        }

        unset($value);
        return $source;
    }


