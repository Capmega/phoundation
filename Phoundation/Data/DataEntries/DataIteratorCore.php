    /**
     * Apply the specified data source over the current source
     *
     * @param bool                           $require_clean_source
     * @param ValidatorInterface|array|null &$source
     *
     * @return static
     */
    public function apply(bool $require_clean_source = true, ValidatorInterface|array|null &$source = null): static
    {
        if ($this->readonly) {
            throw new DataEntryReadonlyException(tr('Cannot apply changes to ":name" object, the object is readonly', [
                ':name' => static::class,
            ]));
        }

        // Place source in a validator
        $o_validator = Validator::pick($source);
        $source    = Arrays::groupByPrefix($o_validator->getSource(), non_prefix_action: Arrays::GROUP_BY_DROP);

        // First, ensure we have all the entries specified by the source
        if ($require_clean_source) {
            try {
                foreach ($source as $data_entry_id => $data_entry_source) {
                    $this->get($data_entry_id);
                }

            } catch (NotExistsException $e) {
                throw new ValidationFailedException(tr('Cannot apply changes to ":id", the specified DataEntry does not exist', [
                    ':id' => $data_entry_id,
                ]), $e);
            }
        }

        // Apply will first validate, so we know ALL has been validated before we're saving
        foreach ($source as $data_entry_id => $data_entry_source) {
            $this->get($data_entry_id, $require_clean_source)?->apply(true, $data_entry_source);
        }

        // Require clean source
        if ($require_clean_source) {
            $o_validator->validate($require_clean_source);
        }

        return $this;
    }


    /**
     * Saves each DataEntry object stored in this DataIterator
     *
     * @param bool        $force
     * @param bool        $skip_validation
     * @param string|null $comments
     *
     * @return static
     */
    public function save(bool $force = false, bool $skip_validation = false, ?string $comments = null): static
    {
        $this->saved_count = 0;

        foreach ($this as $entry) {
            if ($entry) {
                $entry->save($force, $skip_validation, $comments);

                if ($entry->isSaved()) {
                    $this->saved_count++;
                }
            }
        }

        return $this;
    }


