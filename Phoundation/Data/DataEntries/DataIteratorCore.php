    /**
     * Add the specified data entry to the end of the source list
     *
     * @note if no key was specified, the entry will be assigned as-if a new array entry
     *
     * @param mixed                            $value
     * @param Stringable|string|float|int|null $key
     * @param bool                             $skip_null_values
     * @param bool                             $exception
     *
     * @return static
     */
    public function append(mixed $value, Stringable|string|float|int|null $key = null, bool $skip_null_values = true, bool $exception = true): static
    {
        // Skip NULL values?
        if ($value === null) {
            if ($skip_null_values) {
                return $this;
            }
        }

        $key = $this->prepareKey($value, $key);

        return parent::append($value, $key, $skip_null_values, $exception);
    }


    /**
     * Add the specified data entry to the beginning of the source list
     *
     * @note if no key was specified, the entry will be assigned as-if a new array entry
     *
     * @param mixed                      $value
     * @param Stringable|string|float|int|null $key
     * @param bool                       $skip_null_values
     * @param bool                       $exception
     *
     * @return static
     */
    public function prepend(mixed $value, Stringable|string|float|int|null $key = null, bool $skip_null_values = true, bool $exception = true): static
    {
        // Skip NULL values?
        if ($value === null) {
            if ($skip_null_values) {
                return $this;
            }
        }

        $key = $this->prepareKey($value, $key);

        return parent::prepend($value, $key, $skip_null_values, $exception);
    }


    /**
     * Prepares the value to be added to the source
     *
     * @param mixed                            $value
     * @param Stringable|string|float|int|null $key
     *
     * @return string|int
     */
    protected function prepareKey(mixed $value, Stringable|string|float|int|null $key = null): string|int
    {
        // If the value is a DataEntry object, make sure its saved
        if ($value instanceof DataEntryInterface) {
            // If this DataEntry object is NOT modified, DataEntry::save() will automatically not save, this is ok
            $value->save();

        } else {
            // Value might be NULL if we skip NULLs?
            if (is_data_scalar($value)) {
                // Try to load the specified value from the database, assuming $value is the unique identifier
                try {
                    $value = static::getDefaultContentDataType()::new()
                                                                ->setMetaEnabled($this->getMetaEnabled())
                                                                ->load($value);

                } catch (DataEntryNotExistsException $e) {
                    throw new DataEntryNotExistsException(tr('Cannot add specified value ":value" it must be an instance of DataEntryInterface or a unique identifier value for the class ":class"', [
                        ':value' => $value,
                        ':class' => static::getDefaultContentDataType()
                    ]), $e);
                }

                // Now we have a DataEntryInterface type value, we can continue, yay!

            } else {
                if ($value === null) {
                    return $key;
                }

                throw new OutOfBoundsException(tr('Cannot add specified value ":value" it must be an instance of DataEntryInterface', [
                    ':value' => $value,
                ]));
            }
        }

        if ($this->keys_are_unique_column) {
            if ($key) {
                if ($this->require_key_match_unique_identifier) {
                    if (!$value->isNew() and ($key != $value->getUniqueColumnValue())) {
                        // Key must either not be specified or match the id of the DataEntry
                        throw new OutOfBoundsException(tr('Cannot add ":class" class DataEntry with id ":id", the specified key ":key" must either match the value ":value" of the unique column ":unique" or be null', [
                            ':value'  => $value->getUniqueColumnValue(),
                            ':unique' => $value::getUniqueColumn(),
                            ':class'  => $value::class,
                            ':id'     => $value->getId() ?? 'N/A',
                            ':key'    => $key,
                        ]));
                    }
                }

                // Either the specified DataEntry object has no value for its unique column, or the unique column
                // matches the specified key. Either way, we're good to go

            } else {
                $key = $value->getUniqueColumnValue();

                if (!$key) {
                    throw new OutOfBoundsException(tr('Cannot add entry ":value" because the ":class" DataIterator object should use unique column ":column" as keys, but has no unique column value', [
                        ':column' => $value->getUniqueColumn(),
                        ':value'  => $value,
                        ':class'  => static::class,
                    ]));
                }
            }

        } else {
            if ($key) {
                if ($this->require_key_match_unique_identifier) {
                    if ($key != $value->getId()) {
                        // Key must either not be specified or match the id of the DataEntry
                        throw new OutOfBoundsException(tr('Cannot add ":class" class DataEntry with id ":id" and unique column value ":unique", the specified key ":key" must either match the id, unique column value, or be null', [
                            ':class'  => $value::class,
                            ':id'     => $value->getId(),
                            ':unique' => $value->getUniqueColumnValue(),
                            ':key'    => $key,
                        ]));
                    }
                }

                // Either the specified DataEntry object is new or the id matches the specified key, we're good to go
            } else {
                // Key was not specified, use the ID from the DataEntry object as key
                $key = $value->getId();
            }
        }

        return $key;
    }


    /**
     * Clears all entries in this DataIterator, but also all entries in this iterator from the table
     *
     * @note: Clearing means that the entries will NOT be deleted, it will set the status for all entries to "deleted"
     *        and update the unique column to NULL
     *
     * @return static
     */
    public function clearIteratorFromTable(): static
    {
        foreach ($this->source as $entry) {
            $this->ensureObject($entry)
                     ->setStatus('deleted')
                     ->setUniqueColumnValue(null)
                     ->save();
        }

        return parent::clear();
    }


    /**
     * Clears all entries in this DataIterator, but also ALL entries from the table
     *
     * @param string|bool|null $status
     * @todo This method is utterly useless as its only use is deleting every entry in a database. Rewrite the entire setStatus set methods
     *       There should be 2 methods: setStatus on the current entry in the table, and setStatus on all entries specified in a query
     *
     * @return static
     */
    public function deleteAllFromTable(string|bool|null $status = false): static
    {
        if ($status === false) {
            $results = sql()->listKeyValues('SELECT `id` FROM `' . static::getTable() . '`');

        } else {
            $execute = [':status' => $status];
            $results = sql()->listKeyValues('SELECT `id` FROM `' . static::getTable() . '` WHERE' . SqlQueries::is('status', $status, 'status', $execute), $execute);
        }

        if ($results) {
            $this->source = $results;

            foreach ($results as $entry) {
                Log::action(ts('Deleting ":class" entry with id ":id"', [
                    ':class' => static::class,
                    ':id'    => $entry['id'],
                ]), 2);

                $this->ensureObject($entry['id'])
                     ->setStatus('deleted')
                     ->setUniqueColumnValue(null)
                     ->save();
            }

            return parent::clear();
        }

        return $this;
    }


    /**
     * Creates a new DataEntry object and returns it
     *
     * @param IdentifierInterface|array|string|int|null $identifier
     *
     * @return DataEntryInterface
     */
    protected function loadObject(IdentifierInterface|array|string|int|null $identifier): DataEntryInterface
    {
        return $this->getAcceptedDataType()::new()
                                           ->setDebug($this->debug)
                                           ->setMetaEnabled($this->getMetaEnabled())
                                           ->setRestrictionsObject($this->o_restrictions)
                                           ->setConnectorObject($this->getConnectorObject())
                                           ->loadOrThis($identifier);
    }


    /**
     * Ensure the entry we're going to return is from DataEntryInterface interface
     *
     * @param string|float|int $key
     * @param bool             $force
     *
     * @return DataEntryInterface|null
     */
    #[ReturnTypeWillChange] protected function ensureObject(string|float|int $key, bool $force = false): mixed
    {
        if ($this->ensure_objects) {
            // Ensure the source key is of DataEntryInterface
            if (!$this->source[$key] instanceof DataEntryInterface) {
                if (is_array($this->source[$key])) {
                    // Source is stored as an array. Create a new DataEntry and copy the source array into the entry
                    if ($this->inject_source_directly) {
                        $entry = $this->getAcceptedDataType()::new()
                                                             ->setDebug($this->getDebug())
                                                             ->setMetaEnabled($this->getMetaEnabled())
                                                             ->setSourceDirect($this->source[$key]);

                    } else {
                        $entry = $this->getAcceptedDataType()::new()
                                                             ->setDebug($this->getDebug())
                                                             ->setMetaEnabled($this->getMetaEnabled())
                                                             ->setSource($this->source[$key]);
                    }

                } elseif ($this->source[$key] === null) {
                    return null;

                } else {
                    // Load the entry manually from DB. REQUIRES the DataEntry object to have a unique column specified!
                    $entry = $this->loadObject($this->source[$key]);
                }

                $this->source[$key] = $entry->setDebug($this->debug)
                                            ->setColumns($this->columns);
            }
        }

        return $this->source[$key];
    }


