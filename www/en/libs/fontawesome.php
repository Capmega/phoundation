/*
 * Create HTML <select> with all fontawesome classes
 */
function fontawesome_select($params) {
    try{
        Arrays::ensure($params);
        array_default($params, 'sort', true);
        array_default($params, 'none', tr('Select a fontawesome icon'));

        $html = cache_read('select'.isset_get($params['selected']), 'fontawesome');
        if($html) return $html;

        load_libs('html');

        $params['resource'] = fontawesome_array();

        if($params['sort']) {
            ksort($params['resource']);
        }

        $html = html_select($params);

        return cache_write($html, 'select'.isset_get($params['selected']), 'fontawesome');

    }catch(Exception $e) {
        throw new CoreException('fontawesome_select(): Failed', $e);
    }
}



/*
 * Create a target, but don't put anything in it, and return path+filename without extension
 */
